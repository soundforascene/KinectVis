//--------------------------------------------------------------
void ofApp::keyPressed (int key) {
	switch (key) {
			
		case OF_KEY_UP:
			angle++;
			if(angle>30) angle=30;
			kinect.setCameraTiltAngle(angle);
			break;
			
		case OF_KEY_DOWN:
			angle--;
			if(angle<-30) angle=-30;
			kinect.setCameraTiltAngle(angle);
			break;
            
        case 'w':
            translateX * 20;
            break;
        
        case 'd':
            translateY * 20;
            break;
        case 'a':
            translateY / 20;
            break;
        case 's':
            translateY * 20;
            break;
	}
}
